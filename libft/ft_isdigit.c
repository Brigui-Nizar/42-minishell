#include "libft.h"

/*
 * Check if "c" is digit : [0,...,9]
 * @param int 
 * @return int 
 * - isdigit ? 1 : 0"
 */
int	ft_isdigit(int c)
{
	if (47 < c && c < 58)
		return (1);
	return (0);
}
